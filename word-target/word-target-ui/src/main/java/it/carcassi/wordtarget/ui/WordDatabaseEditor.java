    private String createLegendText() {
        StringBuilder sb = new StringBuilder("<html>");
        for (LinkType type : LinkType.values()) {
            String rgb = String.format("#%06X", LinkTypeColor.toColor(type).getRGB() & 0xFFFFFF);
            sb.append("<p style=\"color:").append(rgb).append("\">").append(type).append("</p>");
        }
        sb.append("</html>");
        return sb.toString();
    }
    
